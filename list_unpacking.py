#We can unpack values from a list in to a specific variables.

#In this example we are unpacking the list into 3 variables.
#The first value in the list is assigned to the first variable
#The second value in the list is assigned to the second variable
#The third value in the list is assigned to the third variable
user = ["Joe", "Bucky", 42]
first, second, third = user
print(first)
print(second)
print(third)